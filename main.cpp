short fCount = 10;
ulong pFrame = 0;

void update(){
	handleGameRender(game.screen);
	if(play.fraps && !play.win && !paused && game.screen == 2){
		stringstream ss;
		ss << "fps: " << cFrameRate;
		gms.frames.text = (char*)(ss.str().c_str());
		drawText(game, gms.frames);
	}
	XCopyArea(game.dsp, Buffy, game.win, game.gc, 0, 0, game.scrW, game.scrH, 0, 0);
	XFlush(game.dsp);
}

int main(){
	srand (time(NULL));

	//connect to Xserver
	game.dsp = XOpenDisplay(0);
	if(!game.dsp){
		fprintf(stderr, "can't connect to display");
		return 1;
	}

	init();

	Window rWin = DefaultRootWindow(game.dsp);
	XReadBitmapFile(game.dsp, rWin, "mik.xbm", &bw, &bh, &fineFace, &hx, &hy);
	XReadBitmapFile(game.dsp, rWin, "mik2.xbm", &bw, &bh, &hurtFace, &hx, &hy);
	XReadBitmapFile(game.dsp, rWin, "start.xbm", &sw, &sh, &startSC, &hx, &hy);
	XReadBitmapFile(game.dsp, rWin, "win.xbm", &sw, &sh, &winSC, &hx, &hy);
	XReadBitmapFile(game.dsp, rWin, "lose.xbm", &sw, &sh, &loseSC, &hx, &hy);

	ulong prevTime = 0;
	ulong curTime;

	while(loop){
		//gonna process whatever's pending
		if(XPending(game.dsp)){
			XNextEvent(game.dsp, &ev);
			handleEv(ev);
		}

		//is it time to update?
		curTime = now();
		if((curTime - prevTime) >= (uSec/fps)){
			if(fCount <= 0){
				fCount = 10;
				cFrameRate = (10*uSec)/pFrame;
				pFrame = 0;
			}else{
				pFrame += curTime - prevTime;
				fCount--;
			}
			update();
			prevTime = now();
		}

		//don't rest if there is still stuff to do
		if(!XPending(game.dsp)){
			usleep((uSec/fps)-(curTime-prevTime));
		}
	}

	XCloseDisplay(game.dsp);
	return 0;
}
