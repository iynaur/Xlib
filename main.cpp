string c2hex(float r, float g, float b){
	int red = (int)r;
	int green = (int)g;
	int blue = (int)b;
	int decInt = (red*65536)+(green*256)+blue;
	stringstream ss;
	ss << "#" << setfill('0') << setw(6) << hex << decInt;
	return ss.str();
}

